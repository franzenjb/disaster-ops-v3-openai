// Helper function to get all counties for a region
export function getRegionCounties(regionName: string): string[] {
  const region = redCrossRegionsData[regionName];
  return region ? Object.keys(region.counties) : [];
}

// Helper to get chapter for a county
export function getCountyChapter(regionName: string, countyName: string): string | null {
  const region = redCrossRegionsData[regionName];
  return region?.counties[countyName] || null;
}

// Get total county count
export function getTotalCounties(): number {
  return Object.values(redCrossRegionsData).reduce(
    (total, region) => total + region.countyCount, 
    0
  );
}
